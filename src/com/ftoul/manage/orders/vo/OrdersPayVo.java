package com.ftoul.manage.orders.vo;

public class OrdersPayVo {
	
	private String serialNumber;
	private String payTime;
	private String payType;
	private String payPrice;
	private String payStatic;
	private String orderNumber;
	private String coinNumber;
	private String coinPrice;
	
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getPayTime() {
		return payTime;
	}
	public void setPayTime(String payTime) {
		this.payTime = payTime;
	}
	public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public String getPayPrice() {
		return payPrice;
	}
	public void setPayPrice(String payPrice) {
		this.payPrice = payPrice;
	}
	public String getPayStatic() {
		return payStatic;
	}
	public void setPayStatic(String payStatic) {
		this.payStatic = payStatic;
	}
	public String getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}
	public String getCoinNumber() {
		return coinNumber;
	}
	public void setCoinNumber(String coinNumber) {
		this.coinNumber = coinNumber;
	}
	public String getCoinPrice() {
		return coinPrice;
	}
	public void setCoinPrice(String coinPrice) {
		this.coinPrice = coinPrice;
	}
	
}
