package com.ftoul.web.vo;

public class OrderStaticCountVo {
	
	private String waitPaymentCount;
	private String waitShipmentsCount;
	private String waitReceiptCount;
	private String afterCount;
	private String waitCommentCount;
	
	public String getWaitPaymentCount() {
		return waitPaymentCount;
	}
	public void setWaitPaymentCount(String waitPaymentCount) {
		this.waitPaymentCount = waitPaymentCount;
	}
	public String getWaitShipmentsCount() {
		return waitShipmentsCount;
	}
	public void setWaitShipmentsCount(String waitShipmentsCount) {
		this.waitShipmentsCount = waitShipmentsCount;
	}
	public String getWaitReceiptCount() {
		return waitReceiptCount;
	}
	public void setWaitReceiptCount(String waitReceiptCount) {
		this.waitReceiptCount = waitReceiptCount;
	}
	public String getAfterCount() {
		return afterCount;
	}
	public void setAfterCount(String afterCount) {
		this.afterCount = afterCount;
	}
	public String getWaitCommentCount() {
		return waitCommentCount;
	}
	public void setWaitCommentCount(String waitCommentCount) {
		this.waitCommentCount = waitCommentCount;
	}
	

}
